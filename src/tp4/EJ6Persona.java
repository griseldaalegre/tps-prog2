package tp4;

public class EJ6Persona {
    private String nombre;
    private String apellido;
    private int edad;

    public EJ6Persona(String nombre, String apellido, int edad) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public int getEdad() {
        return edad;
    }




}
